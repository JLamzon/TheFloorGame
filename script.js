let images = [];
let currentPlayer = 1;
let timer1 = 100;
let timer2 = 100;
let answer = "";
let currentImageIndex = 0;
let player1Interval, player2Interval;
let popupWindow = null;

document.getElementById("start-game").addEventListener("click", startGame);
